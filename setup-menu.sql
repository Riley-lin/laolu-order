--   老闆在看單台按「售完」→ 所有客人的點餐頁同步變灰。
-- ================================================================

-- 1️⃣ 建表：一列＝一個品項
create table if not exists public.menu_items (
  id          bigint generated always as identity primary key,
  category_id text    not null,              -- 分類代號（p10 / combo25 / noodle…，跟網頁的分類對齊）
  name        text    not null,              -- 品項名（蘿蔔、貢丸…）
  price       int     not null,              -- 單價（任選組合＝每份價；麵食＝各自價）
  sold_out    boolean not null default false,-- ⭐ 今日售完開關（老闆看單台切換）
  sort        int     not null default 0,    -- 顯示順序
  unique (category_id, name)                 -- 同分類不能有兩個同名品項（重跑保護也靠它）
);

-- 2️⃣ RLS 門禁：誰能看、誰能改
alter table public.menu_items enable row level security;

-- 所有人（含未登入的客人）都能「讀」菜單——點餐頁要顯示
drop policy if exists "menu_read_all" on public.menu_items;
create policy "menu_read_all" on public.menu_items
  for select to anon, authenticated using (true);

-- 只有「登入的老闆」能改（售完/恢復）；匿名客人改不了
drop policy if exists "menu_update_boss" on public.menu_items;
create policy "menu_update_boss" on public.menu_items
  for update to authenticated using (true) with check (true);

